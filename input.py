# haremos un programa interactivo solicitando informacion al usuario.
print("¿como te llamas?")
# utilizaremos otra funcion incorporada llamada input que permite obtener datos de entrada del teclado.

# las funciones pueden retornar valores, y esos valores los podemos guardar en la memoria del computador utilizando inicialmente variables.
# asignando el valor de retorno de la funcion input a una variable de nombre name.
